// ACTIONS & UTILS
// ─────────────────────────────────────────────────────────────────────────────

window.goToSection = (index) => {
    renderSectionView(index);
}

window.renderHome = renderHome;

window.navigate = (direction) => {
    const { currentSectionIndex, currentStepIndex } = state;
    const section = tutorialData[currentSectionIndex];

    if (direction === 'next' && currentStepIndex < section.steps.length - 1) {
        updateStep(currentStepIndex + 1);
    } else if (direction === 'prev' && currentStepIndex > 0) {
        updateStep(currentStepIndex - 1);
    }
}

// 3D Tilt Effect adapted for single item
function init3DTilt() {
    const wrapper = document.querySelector('.iphone-frame-wrapper');
    if (!wrapper || window.innerWidth < 1024) return; // Disable on mobile

    wrapper.addEventListener('mousemove', (e) => {
        const rect = wrapper.getBoundingClientRect();
        const x = e.clientX - rect.left;
        const y = e.clientY - rect.top;

        // Calculate percentages (-1 to 1)
        const xPct = (x / rect.width - 0.5) * 2;
        const yPct = (y / rect.height - 0.5) * 2;

        gsap.to(wrapper, {
            rotationY: xPct * 15,
            rotationX: -yPct * 15,
            duration: 0.5,
            ease: 'power2.out',
            transformPerspective: 1000
        });
    });

    wrapper.addEventListener('mouseleave', () => {
        gsap.to(wrapper, {
            rotationY: 0,
            rotationX: 0,
            duration: 0.6,
            ease: 'elastic.out(1, 0.8)'
        });
    });
}

