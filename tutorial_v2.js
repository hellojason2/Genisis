function updateStep(stepIndex, animate = true) {
    const section = tutorialData[state.currentSectionIndex];
    const step = section.steps[stepIndex];
    const container = document.getElementById('step-container');
    const direction = stepIndex > state.currentStepIndex ? 'next' : 'prev';

    state.currentStepIndex = stepIndex;

    // Update Dots
    const dotsContainer = document.getElementById('progress-dots');
    dotsContainer.innerHTML = section.steps.map((_, idx) => `
        <div class="h-1.5 rounded-full transition-all duration-300 ${idx === stepIndex ? 'w-8 bg-amber-500' : (idx < stepIndex ? 'w-1.5 bg-amber-500/50' : 'w-1.5 bg-white/10')}"></div>
    `).join('');

    // Update Counter
    document.getElementById('step-counter').textContent = `Step ${stepIndex + 1} / ${section.steps.length}`;

    // Update Buttons
    const btnPrev = document.getElementById('btn-prev');
    const btnNext = document.getElementById('btn-next');

    btnPrev.disabled = stepIndex === 0;

    if (stepIndex === section.steps.length - 1) {
        btnNext.innerHTML = `<span>Complete</span><iconify-icon icon="ph:check-bold"></iconify-icon>`;
        btnNext.classList.replace('bg-amber-500', 'bg-green-500');
        btnNext.onclick = () => renderHome();
    } else {
        btnNext.innerHTML = `<span>Next Step</span><iconify-icon icon="ph:arrow-right-bold"></iconify-icon>`;
        btnNext.classList.replace('bg-green-500', 'bg-amber-500');
        btnNext.onclick = () => navigate('next');
    }

    // Content HTML
    const contentHtml = `
        <!-- Image Side -->
        <div class="order-2 lg:order-1 relative group perspective-container flex justify-center">
           <div class="iphone-frame-wrapper transform transition-all duration-500 hover:scale-105">
                <div class="relative bg-black rounded-[2.5rem] p-3 shadow-2xl border border-white/10 ring-1 ring-black">
                     <!-- Screen -->
                     <div class="relative rounded-[2rem] overflow-hidden aspect-[9/19.5] bg-[#050505]">
                         <img src="${step.image}" class="w-full h-full object-cover" alt="${step.title}">
                         <!-- Home Indicator -->
                         <div class="absolute bottom-2 left-1/2 -translate-x-1/2 w-1/3 h-1 bg-white/20 rounded-full backdrop-blur-md"></div>
                     </div>
                     <!-- Reflection Glare -->
                     <div class="absolute inset-0 rounded-[2.5rem] bg-gradient-to-tr from-white/5 to-transparent pointer-events-none"></div>
                </div>
           </div>
        </div>

        <!-- Text Side -->
        <div class="order-1 lg:order-2 space-y-6">
            <div class="inline-flex items-center gap-2 px-3 py-1 rounded-lg bg-amber-500/10 text-amber-500 text-xs font-bold uppercase tracking-wider">
                Step ${stepIndex + 1}
            </div>
            
            <h2 class="text-4xl md:text-5xl font-bold text-white leading-tight">
                ${step.title}
            </h2>
            
            <p class="text-lg text-neutral-400 leading-relaxed border-l-2 border-white/10 pl-6">
                ${step.description}
            </p>
        </div>
    `;

    // Animate Transition
    if (animate) {
        gsap.to(container, {
            opacity: 0,
            x: direction === 'next' ? -20 : 20,
            duration: 0.2,
            onComplete: () => {
                container.innerHTML = contentHtml;
                init3DTilt(); // Re-init tilt on new element
                gsap.fromTo(container,
                    { opacity: 0, x: direction === 'next' ? 20 : -20 },
                    { opacity: 1, x: 0, duration: 0.3, ease: 'power2.out' }
                );
            }
        });
    } else {
        container.innerHTML = contentHtml;
        init3DTilt();
    }
}

